function collectTexts(page) {
  const texts = [...textFragmentsForTranslation(page.pageTitle), ...textFragmentsForTranslation(page.title)];
  const skipInteractiveDetails = Boolean(INTERACTIVE_TOOL_PAGE_NOTES[page.file]);
  for (const block of page.blocks ?? []) {
    if (block.type === 'heading' || block.type === 'paragraph') {
      texts.push(...textFragmentsForTranslation(block.text));
    } else if (block.type === 'list') {
      if (isRedundantHeadingList(page, block)) continue;
      for (const item of block.items) texts.push(...textFragmentsForTranslation(item));
    } else if (!skipInteractiveDetails && block.type === 'table' && !shouldUseSourceOnlyTableCells(page)) {
      for (const row of block.rows) {
        for (const cell of row) {
          texts.push(...textFragmentsForTranslation(cell).filter(shouldTranslateTableCell));
        }
      }
    }
  }
  return texts;
}

function slugify(value) {
  return String(value)
    .toLowerCase()
    .replace(/[^a-z0-9\u4e00-\u9fff]+/gi, '-')
    .replace(/^-+|-+$/g, '')
    .slice(0, 80);
}

function fileStem(file) {
  return file.replace(/\.html$/, '');
}

function escapeMarkdownCell(value) {
  return String(value || ' ')
    .replace(/\|/g, '\\|')
    .replace(/_/g, '\\_')
    .replace(/\n+/g, '<br>')
    .trim();
}

function shouldTranslateTableCell(value) {
  const text = String(value ?? '');
  return hasJapaneseKana(text);
}

function shouldUseSourceOnlyTableCells(page) {
  return page.file === 'recipe.html';
}

function escapeMarkdownAlt(value) {
  return String(value || 'FC2 image')
    .replace(/[\[\]\n\r]/g, ' ')
    .replace(/\s{2,}/g, ' ')
    .trim();
}

function isFc2ContentImageUrl(value) {
  try {
    const url = new URL(value);
    return url.origin === 'https://atelier3.web.fc2.com' && url.pathname.startsWith('/ngo/') && /\.(?:png|jpe?g|gif)$/i.test(url.pathname);
  } catch {
    return false;
  }
}

function renderImageToken(page, index, imageByUrl) {
  const image = page.images?.[Number(index)];
  if (!image) throw new Error(`Missing image token FC2_IMAGE_${index} in ${page.file}.`);

  const manifestImage = imageByUrl.get(image.url);
  if (!manifestImage) {
    if (isFc2ContentImageUrl(image.url)) {
      throw new Error(`Missing synced FC2 image for ${image.url}. Run npm run sync:fc2-images.`);
    }
    return '';
  }

  const alt = escapeMarkdownAlt(image.alt || image.title || image.file || 'FC2 image');
  return `![${alt}](${manifestImage.publicPath})`;
}

function renderImagesInText(value, page, imageByUrl) {
  return String(value ?? '')
    .replace(IMAGE_TOKEN_PATTERN, (_, index) => renderImageToken(page, index, imageByUrl))
    .replace(/\s{2,}/g, ' ')
    .trim();
}

function tableCellText(value, translator, page, imageByUrl) {
  const sourceOnly = shouldUseSourceOnlyTableCells(page);
  const text = localizeClassOnlyText(
    sourceOnly || !shouldTranslateTableCell(value) ? translator.local(value) : translator.get(value),
  );
  return renderImagesInText(text, page, imageByUrl);
}

function cleanHeadingText(value) {
  return value.replace(/[!！?？。．.：:；;、,\s]+$/g, '').trim();
}

function isRedundantHeadingList(page, block) {
  if (block.type !== 'list' || block.ordered || block.items.length < 4) return false;
  const sourceHeadings = new Set(
    (page.blocks ?? [])
      .filter((item) => item.type === 'heading' && item.level > 1)
      .map((item) => cleanHeadingText(item.text)),
  );
  if (sourceHeadings.size === 0) return false;
  const matchingItems = block.items.filter((item) => sourceHeadings.has(cleanHeadingText(item))).length;
  return matchingItems >= 4 && matchingItems / block.items.length >= 0.8;
}

function renderMarkdownTable(rows, translator, page, imageByUrl) {
  const maxColumns = Math.max(...rows.map((row) => row.length));
  const normalized = rows.map((row) => {
    const cells = row.map((cell) => tableCellText(cell, translator, page, imageByUrl));
    while (cells.length < maxColumns) cells.push('');
    return cells;
  });

  const header = normalized[0].map((cell, index) => escapeMarkdownCell(cell || `欄位 ${index + 1}`));
  const body = normalized.slice(1).map((row) => `|${row.map(escapeMarkdownCell).join('|')}|`);
  return [`|${header.join('|')}|`, `|${header.map(() => '---').join('|')}|`, ...body].join('\n');
}

function hasLeadingRowspanImageColumn(rows) {
  if (rows.length < 4) return false;
  const maxColumns = Math.max(...rows.map((row) => row.length));
  const header = rows[0] ?? [];
  const firstDataRow = rows[1] ?? [];
  if (maxColumns < 4 || !/^(?:画像|圖片|image)$/i.test(header[0] ?? '')) return false;
  if (!hasImageToken(firstDataRow[0] ?? '')) return false;

  const shiftedRows = rows
    .slice(2)
    .filter(
      (row) =>
        (row.length === maxColumns - 1 && !hasImageToken(row[0] ?? '')) ||
        (row.length === maxColumns && !String(row[0] ?? '').trim() && !hasImageToken(row[0] ?? '')),
    );
  return shiftedRows.length >= 2 && shiftedRows.length / Math.max(rows.length - 2, 1) >= 0.6;
}

function tableWithoutLeadingRowspanImageColumn(rows) {
  const maxColumns = Math.max(...rows.map((row) => row.length));
  const header = rows[0].slice(1);
  const firstDataRow = rows[1].slice(1);
  const bodyRows = rows.slice(2).map((row) => (row.length === maxColumns - 1 ? row : row.slice(1)));
  return [header, firstDataRow, ...bodyRows];
}

function renderTable(rows, translator, page, imageByUrl) {
  if (hasLeadingRowspanImageColumn(rows)) {
    const image = tableCellText(rows[1][0], translator, page, imageByUrl);
    const table = renderMarkdownTable(tableWithoutLeadingRowspanImageColumn(rows), translator, page, imageByUrl);
    return [image, table].filter(Boolean).join('\n\n');
  }

  return renderMarkdownTable(rows, translator, page, imageByUrl);
}

function isSetDetailPage(page) {
  return String(page.title ?? '').startsWith('セット/');
}

function setDetailTableHeading(rows, page) {
  if (!isSetDetailPage(page) || rows.length === 0) return '';
  const header = rows[0].map((cell) => String(cell ?? '').trim());
  if (header[0] === 'セット数' && header[1] === '効果') return '套裝效果';
  if (header[0] !== '名前' || !header.includes('Mods')) return '';
  if (header.includes('ダメージ') || header.includes('スピード') || header.includes('DPS')) return '武器';
  if (header.includes('物理防御')) return '防具';
  return '';
}

function renderPage(page, translator, imageByUrl) {
  const lines = [
    `<a id="fc2-${fileStem(page.file)}"></a>`,
    '',
    `## ${localizeClassOnlyText(translator.get(page.pageTitle))}`,
    '',
    `- FC2 file：[${page.file}](${page.url})`,
  ];
  if (page.lastModified) lines.push(`- 原站 Last-Modified：\`${page.lastModified}\``);
  lines.push('');

  const interactiveNote = INTERACTIVE_TOOL_PAGE_NOTES[page.file];
  if (interactiveNote) {
    for (const block of page.blocks ?? []) {
      if (block.type !== 'paragraph') continue;
      const text = renderImagesInText(translator.get(block.text), page, imageByUrl);
      if (text) lines.push(text, '');
    }
    lines.push('> **原站互動工具**');
    for (const note of interactiveNote) lines.push(`> ${note}`);
    lines.push(`> 原站連結：[${localizeClassOnlyText(translator.get(page.pageTitle))}](${page.url})。`);
    return lines.join('\n').replace(/\n{3,}/g, '\n\n').trim();
  }

  let skippedFirstH1 = false;
  const headingIds = new Map();
  let baseSourceHeadingLevel = null;
  for (const block of page.blocks ?? []) {
    if (block.type === 'heading') {
      if (!skippedFirstH1 && block.level === 1) {
        skippedFirstH1 = true;
        continue;
      }
      const headingText = cleanHeadingText(renderImagesInText(localizeClassOnlyText(translator.get(block.text)), page, imageByUrl));
      if (baseSourceHeadingLevel === null) baseSourceHeadingLevel = block.level;
      const baseId = block.id || slugify(headingText) || `section-${headingIds.size + 1}`;
      const idKey = `${fileStem(page.file)}-${baseId}`;
      const count = (headingIds.get(idKey) ?? 0) + 1;
      headingIds.set(idKey, count);
      const id = count === 1 ? idKey : `${idKey}-${count}`;
      const level = Math.min(3 + Math.max(0, block.level - baseSourceHeadingLevel), 6);
      lines.push(`<a id="fc2-${id}"></a>`, '', `${'#'.repeat(level)} ${headingText}`, '');
      continue;
    }

    if (block.type === 'paragraph') {
      const text = renderImagesInText(translator.get(block.text), page, imageByUrl);
      if (text) lines.push(text, '');
      continue;
    }

    if (block.type === 'list') {
      if (isRedundantHeadingList(page, block)) continue;
      const items = block.items.flatMap((item, index) => {
        const marker = block.ordered ? `${index + 1}.` : '-';
        const lines = renderImagesInText(translator.get(item), page, imageByUrl).split('\n').filter(Boolean);
        if (lines.length === 0) return [];
        return [`${marker} ${lines[0]}`, ...lines.slice(1).map((line) => `  ${line}`)];
      });
      if (items.length > 0) lines.push(...items, '');
      continue;
    }

    if (block.type === 'table') {
      if (block.rows.length > 0) {
        const tableHeading = setDetailTableHeading(block.rows, page);
        if (tableHeading) lines.push(`### ${tableHeading}`, '');
        lines.push(renderTable(block.rows, translator, page, imageByUrl), '');
      }
    }
  }

  return lines.join('\n').replace(/\n{3,}/g, '\n\n').trim();
}

function sourceReminder() {
  return [
    '---',
    '',
    '> **版本提醒**',
    '> 本頁是 FC2 玩家攻略快照的繁中整理版；技能、裝備、掉落、配方與版本敏感數值，請以目前遊戲內 tooltip / UI 與官方公告為準。',
  ].join('\n');
}

function introFor(outputFile, sourcePages, reviewedAt) {
  const hasInteractiveToolPage = sourcePages.some((page) => INTERACTIVE_TOOL_PAGE_NOTES[page.file]);
  const sourcePolicy = [
    '本頁由 FC2 / atelier3 原站 HTML 重新擷取後繁中化，保留原頁段落、清單、表格欄位與數值；FC2 內容圖片已本地鏡像，CSS、JavaScript 與追蹤圖不搬入公開站。',
    hasInteractiveToolPage ? '互動工具頁僅保留用途摘要與原站連結。' : '',
  ]
    .filter(Boolean)
    .join('');
  return [
    sourcePolicy,
    '',
    `- 檢視日期：\`${reviewedAt}\``,
    `- FC2 來源頁數：${sourcePages.length}`,
    '- 翻譯策略：職業、屬性、難度、天賦與符文採中文優先；英文 item / skill / boss / map 查詢詞按需保留。',
    '- 內容狀態：由 FC2 原站正文生成，不使用舊版摘要句。',
    '',
    '> **快速重點**',
    '> 這是玩家攻略與資料表的繁中化快照，適合查路線、裝備、符文、Recipe、套裝 / 獨特 / Legendary 數值。',
    '> 投入 crafting、交易或丟裝前，仍建議回遊戲內 tooltip 確認。',
    '',
    `<a id="${outputFile.replace(/\.md$/, '')}-sources"></a>`,
    '',
    '## 本頁收錄來源',
    '',
    '|FC2 file|原站標題|Last-Modified|',
    '|---|---|---|',
    ...sourcePages.map((page) => `|[${page.file}](${page.url})|${escapeMarkdownCell(page.title)}|${page.lastModified ?? 'N/A'}|`),
  ].join('\n');
}

async function main() {
  if (!existsSync(MANIFEST_PATH)) throw new Error(`Missing manifest: ${MANIFEST_PATH}`);
  if (!existsSync(IMAGE_MANIFEST_PATH)) {
    throw new Error(`Missing FC2 image manifest: ${IMAGE_MANIFEST_PATH}. Run npm run sync:fc2-images first.`);
  }
  if (!existsSync(TOPIC_MAP_PATH)) throw new Error(`Missing topic map: ${TOPIC_MAP_PATH}`);
  if (!existsSync(PAGE_CACHE)) throw new Error(`Missing FC2 page cache: ${PAGE_CACHE}. Run npm run crawl:fc2 first.`);

  const manifest = await readJson(MANIFEST_PATH);
  const imageManifest = await readJson(IMAGE_MANIFEST_PATH);
  const topicMap = await readJson(TOPIC_MAP_PATH);
  const manifestByUrl = new Map(manifest.pages.map((page) => [page.url, page]));
  const imageByUrl = new Map((imageManifest.images ?? []).map((image) => [image.url, image]));
  const outputFiles = generatedOutputFiles(topicMap);
  const pagesByFile = new Map();
  const translator = await Translator.load();

  for (const page of manifest.pages) {
    pagesByFile.set(page.file, await pageCacheFor(page.file));
  }

  const texts = [];
  for (const page of pagesByFile.values()) {
    texts.push(...collectTexts(page));
  }
  await translator.translateMany(texts);
  await translator.save();

  const reviewedAt = todayTaipei();
  for (const outputFile of outputFiles) {
    const meta = TOPIC_META[outputFile];
    if (!meta) throw new Error(`Missing generated topic metadata for ${outputFile}`);
    const sourcePages = orderSourcePagesForOutput(outputFile, sourcePagesForOutput(topicMap, manifestByUrl, outputFile));
    const pageSections = sourcePages.map((sourcePage) => renderPage(pagesByFile.get(sourcePage.file), translator, imageByUrl));
    const output = [
      frontmatterFor({
        title: meta.title,
        description: meta.description,
        reviewedAt,
        sourcePages,
      }),
      '',
      introFor(outputFile, sourcePages, reviewedAt),
      '',
      ...pageSections.flatMap((section) => [section, '']),
      sourceReminder(),
      '',
    ].join('\n');

    await mkdir(DOCS_DIR, { recursive: true });
    await writeFile(join(DOCS_DIR, outputFile), output.replace(/\n{4,}/g, '\n\n\n'), 'utf8');
  }

  console.log(
    JSON.stringify(
      {
        generated: outputFiles.length,
        outputFiles,
        sourcePages: manifest.pages.length,
        translationCache: TRANSLATION_CACHE,
      },
      null,
      2,
    ),
  );
}

await main();
